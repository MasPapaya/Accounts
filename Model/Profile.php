	public function exist_user() {
		if (isset($this->data['Profile']['user_id']) && $this->data['Profile']['user_id'] != '') {
			if (!empty($this->data['Profile']['id'])) {
				$exist_user = $this->find('count', array(
					'conditions' => array('Profile.user_id' => $this->data['Profile']['user_id'], 'Profile.id !=' => $this->data['Profile']['id'])
					));
			} else {
				$exist_user = $this->find('count', array(
					'conditions' => array('Profile.user_id' => $this->data['Profile']['user_id'])
					));
			}
			if ($exist_user > 0) {
				return false;
			} else {
				return true;
			}
		} else {
			return true;
		}
		return false;
	}

